package section_18_graphs;

public class GraphsIntroduction {
	/*
	 * Vertex or Node
	 * Edge or Connection
	 * Weighted edges
	 * Non-weighted edges
	 * Directional or Bidirectional edges
	 * Graphs examples: Linked list, Trees
	 * Adjacency matrix: It has 45 degree 0 line. 
	 * Adjacency list: It is represented as Hashmap.
	 * {
	 * "A"=["B","E"],
	 * "B"=["A","C"]
	 * }
	 * 
	 * Big O:
	 * 
	 * Space complexity: Adjacency list is better.
	 * Adjacency matrix: O(|V|^2)
	 * Adjacency list: O(|V|+|E|)
	 * 
	 * Time complexity:
	 * 
	 * Adding a vertex in Adjacency matrix=O(|V|^2)
	 * Adding a vertex in Adjacency list=O(1)
	 * 
	 * Adding an edge in Adjacency matrix=O(1)
	 * Adding an edge in Adjacency list=O(1)
	 * 
	 * Removing an edge in Adjacency matrix=O(1)
	 * Removing an edge in Adjacency list=O(|E|)
	 * 
	 * Removing a vertex in Adjacency matrix=O(|V|^2)
	 * Removing a vertex in Adjacency list=O(|V|+|E|)
	 * 
	 */

}
